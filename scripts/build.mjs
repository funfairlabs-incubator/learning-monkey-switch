// ── Helpers ──────────────────────────────────────────────────────────────────

function read(path) {
  return existsSync(path) ? readFileSync(path, 'utf8') : null;
}

function readJSON(path) {
  const raw = read(path);
  if (!raw) return null;
  try { return JSON.parse(raw); } catch { return null; }
}

function formatDate(iso) {
  try {
    return new Date(iso).toLocaleDateString('en-GB', {
      weekday: 'long', day: 'numeric', month: 'long', year: 'numeric'
    });
  } catch { return iso; }
}

function renderCard(resource) {
  return `
    <a href="${resource.url}" target="_blank" rel="noopener noreferrer" class="resource-card">
      <span class="resource-title">${resource.title}</span>
      <span class="resource-why">${resource.why_now}</span>
      <span class="resource-link-icon">↗</span>
    </a>`;
}

function renderSection(data) {
  if (!data) return '';
  return `
  <section class="topic-section">
    <h2 class="topic-title">${data.topic}</h2>
    <div class="highlight-card">
      <span class="highlight-label">This week's highlight</span>
      <a href="${data.highlight.url}" target="_blank" rel="noopener noreferrer" class="highlight-link">
        ${data.highlight.title} ↗
      </a>
      <p class="highlight-summary">${data.highlight.summary}</p>
    </div>
    <div class="resources-grid">
      ${data.resources.map(renderCard).join('')}
    </div>
    <div class="claude-take">
      <span class="take-label">Claude's take</span>
      <p>${data.claude_take}</p>
    </div>
    <div class="section-meta">Updated ${formatDate(data.generated_at)}</div>
  </section>`;
}

